    public class MyLocationListenner implements BDLocationListener {

        @Override
        public void onReceiveLocation(BDLocation location) {
            // map view 销毁后不在处理新接收的位置
            if (location == null || mMapView == null)
                return;
            MyLocationData locData = new MyLocationData.Builder()
                    .accuracy(location.getRadius())
                            // 此处设置开发者获取到的方向信息，顺时针0-360
                    .direction(100).latitude(location.getLatitude())
                    .longitude(location.getLongitude()).build();
            mBaiduMap.setMyLocationData(locData);
            if (isFirstLoc) {
                isFirstLoc = false;
                LatLng ll = new LatLng(location.getLatitude(),
                        location.getLongitude());
                MapStatusUpdate u = MapStatusUpdateFactory.newLatLng(ll);
                mBaiduMap.animateMapStatus(u);
                LatLng pt = new LatLng(location.getLatitude(), location.getLongitude());
                BitmapDescriptor bd = BitmapDescriptorFactory.fromResource(R.drawable.icon_gcoding);
                LatLngBounds.Builder builder = new LatLngBounds.Builder();
                MarkerOptions markerOptions = new MarkerOptions().icon(bd).position(pt);
                mMarker1 = (Marker) (mBaiduMap.addOverlay(markerOptions));
                LatLng a = new LatLng(location.getLatitude(), location.getLongitude());
                PlanNode b = PlanNode.withLocation(a);
                stNode = b;
                String aaa;
                aaa = "" + location.getLongitude() + "," + location.getLatitude();
                new GetWeatherData().execute(aaa);
            }
        }
    }

